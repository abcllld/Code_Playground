public class literals {
    public static void main(String[] args) {
        byte age = 68;
        char ch = 'a';
        float b = 8.98f; //note that by default decimal value are always double and to make it to float we should write 'f' with it
        double d = 79.76; //if one wants we can write d or D with no i.e, 79.76d-->Correct
//        int/short/byte does not need anything more thanthis to write but yes we if we have long int then we have to write l or Li.e,
        long check = 897325927l;
        boolean checkiing = true;
        String str = "Rahul_Mishra";
        System.out.println(age);
        System.out.println(ch);
        System.out.println(b);
        System.out.println(d);
        System.out.println(check);
        System.out.println(checkiing);
        System.out.println(str);



    }
}
