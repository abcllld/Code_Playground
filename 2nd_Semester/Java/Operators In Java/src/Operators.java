

public class Operators {
    public static void main(String[] args) {


//    Arithmetic operators can not work with Boolean as 1+ True or 5 * false does not make any sense
//    modulus operators can also work with float and double value such as 7.8%2.3-->Will return modulus reminder
        int y = 12;
        int x = ++y + 8;
        System.out.println(x);
        System.out.println(y);

        char a = 'B';
        System.out.println(a++); //B
        System.out.println(++a); //D




    }
}
