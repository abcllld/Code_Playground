//  Function Parameter vs Function Arguments
// Function parameters are the names listed in the function's definition.
// Function arguments are the real values passed to the function.


function sum(a,b){ //Here a,b are called FUNCTION PARAMETERS*******
var total= a+b;
console.log(total);
}


sum();     
sum(20,30); 
sum(20,87);  //The value I.E, 20,30 that in passed in the function name under parenthesis separated by comma is called FUNCTION ARGUMENTS***************

// Thus parameters are those which are used inside parenthesis of a function name separated by comma which is used while defining a function

// The arguments are real values which are used while calling if.e, invoking a function


