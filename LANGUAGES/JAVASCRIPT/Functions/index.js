// A JavaScript function is a block of code designed to perform a particular tasks.

// Function Definition 
// Before we use a function, we need to define it. 
// A function definition (also called a function declaration, or function 
// statement) 
// consists of the function keyword, followed by:

// The name of the function.
// A list of parameters to the function, enclosed in parentheses and separated 
// by commas.
// The JavaScript statements that define the function, enclosed in curly 
// brackets, {...}.

// SYNTAX OF FUNCTION

function functionName(){
    // STATEMENT
}

function sum(){
var a = 8, b = 7
var total= a+b;
console.log(total);
}

// The above is defining a function

// Calling functions
// Defining a function does not execute it.
// A JavaScript function is executed when "something" invokes it (calls it).

sum();

// This is called invoking a function





